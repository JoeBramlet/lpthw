# This is a comment. It's here for me, not the computer.
# Python ignores everything after the # symbol.

print("I could have code like this.")  # Python skips this part too.

# I can also turn off a line of code by commenting it out:
# print("This won't run.")

print("This will run.")

# -----------------------------
# Lesson Overview
# -----------------------------
# In this exercise I'm learning how comments work in Python.
# A comment starts with # and Python ignores everything after it.
# This lets me explain what my code is doing without breaking anything.

# -----------------------------
# Why This Matters
# -----------------------------
# Comments help me understand my own code later.
# They also help me stay organized while I'm learning.
# If something looks confusing in the future, a comment can save me.

# -----------------------------
# What I Noticed
# -----------------------------
# - Python prints exactly what I tell it to print.
# - Each line runs on its own, top to bottom.
# - A # symbol instantly turns the rest of the line into "human-only" text.

# -----------------------------
# My Alternate Example
